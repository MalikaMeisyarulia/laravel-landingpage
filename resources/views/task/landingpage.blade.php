<section id="jenis-cookies" class="bg-white p-6 m-4 rounded-lg shadow">
    <h2 class="text-2xl font-bold">Jenis Cookies</h2>
    <ul class="list-disc list-inside">
        <li><strong>Session Cookies:</strong> Cookies yang hanya disimpan sementara selama sesi browsing.</li>
        <li><strong>Persistent Cookies:</strong> Cookies yang disimpan di perangkat pengguna untuk jangka waktu tertentu.</li>
        <li><strong>First-party Cookies:</strong> Cookies yang dibuat oleh website yang sedang dikunjungi.</li>
        <li><strong>Third-party Cookies:</strong> Cookies yang dibuat oleh pihak ketiga, biasanya untuk iklan atau analisis.</li>
    </ul>
</section>

<section id="cara-membuat" class="bg-white p-6 m-4 rounded-lg shadow">
    <h2 class="text-2xl font-bold">Cara Membuat Cookies</h2>
    <p>Berikut adalah resep sederhana untuk membuat cookies cokelat:</p>
    <ol class="list-decimal list-inside">
        <li>Siapkan bahan-bahan: tepung, gula, mentega, telur, dan cokelat chips.</li>
        <li>Campurkan mentega dan gula hingga lembut.</li>
        <li>Tambahkan telur dan aduk rata.</li>
        <li>Masukkan tepung dan cokelat chips, aduk hingga tercampur.</li>
        <li>Sendokkan adonan ke atas loyang dan panggang di oven selama 10-12 menit.</li>
    </ol>
</section>

