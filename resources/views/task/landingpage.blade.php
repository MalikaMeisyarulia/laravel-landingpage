    <h2 class="text-2xl font-bold">Apa Itu Cookies?</h2>
    <p>Cookies adalah file kecil yang disimpan di perangkat pengguna saat mengunjungi suatu website. Cookies digunakan untuk menyimpan informasi tentang sesi pengguna, preferensi, dan aktivitas di website tersebut.</p>
